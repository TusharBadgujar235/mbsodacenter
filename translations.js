// Set default language
let currentLanguage = localStorage.getItem('mbSodaLanguage') || 'en';

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    initializeLanguage();
    setupLanguageToggle();
});

function initializeLanguage() {
    // Set HTML lang attribute
    document.documentElement.lang = currentLanguage;
    
    // Translate all elements with data-en and data-mr attributes
    const elements = document.querySelectorAll('[data-en][data-mr]');
    elements.forEach(el => {
        if (currentLanguage === 'en') {
            el.textContent = el.getAttribute('data-en');
        } else {
            el.textContent = el.getAttribute('data-mr');
        }
    });
    
    // Translate all elements with data-i18n attribute
    const i18nElements = document.querySelectorAll('[data-i18n]');
    i18nElements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLanguage][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[currentLanguage][key];
            } else {
                el.textContent = translations[currentLanguage][key];
            }
        }
    });
    
    // Update language button
    const langBtn = document.getElementById('lang-btn');
    if (langBtn) {
        if (currentLanguage === 'en') {
            langBtn.querySelector('.lang-en').style.display = 'inline';
            langBtn.querySelector('.lang-mr').style.display = 'none';
        } else {
            langBtn.querySelector('.lang-en').style.display = 'none';
            langBtn.querySelector('.lang-mr').style.display = 'inline';
        }
    }
}

function setupLanguageToggle() {
    const langBtn = document.getElementById('lang-btn');
    if (langBtn) {
        langBtn.addEventListener('click', toggleLanguage);
    }
}

function toggleLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'mr' : 'en';
    localStorage.setItem('mbSodaLanguage', currentLanguage);
    initializeLanguage();
}

// Export for use in other scripts
window.currentLanguage = currentLanguage;
window.getTranslation = (key) => translations[currentLanguage][key] || key;
